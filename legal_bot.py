import numpy as np
from sentence_transformers import SentenceTransformer
import faiss

class LegalBot:
    """
    –ö–ª–∞—Å—Å –¥–ª—è —Å–µ–º–∞–Ω—Ç–∏—á–µ—Å–∫–æ–≥–æ –ø–æ–∏—Å–∫–∞ –≤ —é—Ä–∏–¥–∏—á–µ—Å–∫–∏—Ö –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ö
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç RAG-–∞—Ä—Ö–∏—Ç–µ–∫—Ç—É—Ä—É –∏ –≤–µ–∫—Ç–æ—Ä–Ω—ã–π –ø–æ–∏—Å–∫
    """
    
    def __init__(self):
        """
        –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –±–æ—Ç–∞:
        - –ó–∞–≥—Ä—É–∑–∫–∞ –º–æ–¥–µ–ª–∏ –¥–ª—è —Ä—É—Å—Å–∫–æ—è–∑—ã—á–Ω—ã—Ö —Ç–µ–∫—Å—Ç–æ–≤
        - –°–æ–∑–¥–∞–Ω–∏–µ –≤–µ–∫—Ç–æ—Ä–Ω–æ–≥–æ –∏–Ω–¥–µ–∫—Å–∞ –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –ø–æ–∏—Å–∫–∞
        """
        # –ú–æ–¥–µ–ª—å –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –≤–µ–∫—Ç–æ—Ä–Ω—ã—Ö –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–π —Ç–µ–∫—Å—Ç–∞
        self.model = SentenceTransformer('paraphrase-multilingual-MiniLM-L12-v2')
        
        # –•—Ä–∞–Ω–∏–ª–∏—â–µ –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ (–∑–∞–∫–æ–Ω–æ–≤) —Å –º–µ—Ç–∞–¥–∞–Ω–Ω—ã–º–∏
        self.documents = []  # –°–ø–∏—Å–æ–∫ —Ç–µ–∫—Å—Ç–æ–≤ –∑–∞–∫–æ–Ω–æ–≤
        self.metadata = []   # –°–ø–∏—Å–æ–∫ –º–µ—Ç–∞–¥–∞–Ω–Ω—ã—Ö (–Ω–∞–∑–≤–∞–Ω–∏–µ, —Å—Ç–∞—Ç—å—è)
        
        # –í–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å –¥–ª—è —Å–µ–º–∞–Ω—Ç–∏—á–µ—Å–∫–æ–≥–æ –ø–æ–∏—Å–∫–∞ (—Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç—å 384)
        self.vector_index = faiss.IndexFlatL2(384)
        
        print("‚úÖ –Æ—Ä–∏–¥–∏—á–µ—Å–∫–∏–π –±–æ—Ç –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω –∏ –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ!")
    
    def add_law(self, text: str, law_name: str = "", article: str = "") -> None:
        """
        –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –∑–∞–∫–æ–Ω–∞ –≤ –±–∞–∑—É –∑–Ω–∞–Ω–∏–π –±–æ—Ç–∞
        
        Args:
            text (str): –ü–æ–ª–Ω—ã–π —Ç–µ–∫—Å—Ç –∑–∞–∫–æ–Ω–∞ –∏–ª–∏ —Å—Ç–∞—Ç—å–∏
            law_name (str): –ù–∞–∑–≤–∞–Ω–∏–µ –∑–∞–∫–æ–Ω–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, "–¢–ö –†–§")
            article (str): –ù–æ–º–µ—Ä —Å—Ç–∞—Ç—å–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, "—Å—Ç. 80")
        """
        try:
            # –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ –≤ –≤–µ–∫—Ç–æ—Ä–Ω–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ
            vector = self.model.encode([text])
            
            # –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –≤–µ–∫—Ç–æ—Ä–∞ –≤ –∏–Ω–¥–µ–∫—Å
            self.vector_index.add(vector)
            
            # –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç–∞ –∏ –º–µ—Ç–∞–¥–∞–Ω–Ω—ã—Ö
            self.documents.append(text)
            self.metadata.append({
                "law": law_name,
                "article": article,
                "full_reference": f"{law_name}, {article}" if law_name and article else "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π –∏—Å—Ç–æ—á–Ω–∏–∫"
            })
            
            print(f"‚úÖ –ó–∞–∫–æ–Ω –¥–æ–±–∞–≤–ª–µ–Ω: {law_name} {article}")
            
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ –∑–∞–∫–æ–Ω–∞: {e}")
    
    def ask(self, question: str, top_k: int = 3) -> dict:
        """
        –ü–æ–∏—Å–∫ –æ—Ç–≤–µ—Ç–∞ –Ω–∞ —é—Ä–∏–¥–∏—á–µ—Å–∫–∏–π –≤–æ–ø—Ä–æ—Å –≤ –±–∞–∑–µ –∑–∞–∫–æ–Ω–æ–≤
        
        Args:
            question (str): –í–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            top_k (int): –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
            
        Returns:
            dict: –†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞ –≤ —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –≤–∏–¥–µ
        """
        try:
            # –í–µ–∫—Ç–æ—Ä–∏–∑–∞—Ü–∏—è –≤–æ–ø—Ä–æ—Å–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            question_vector = self.model.encode([question])
            
            # –ü–æ–∏—Å–∫ –Ω–∞–∏–±–æ–ª–µ–µ –ø–æ—Ö–æ–∂–∏—Ö –∑–∞–∫–æ–Ω–æ–≤
            distances, indices = self.vector_index.search(question_vector, top_k)
            
            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
            if len(indices[0]) == 0:
                return {
                    "success": False,
                    "message": "–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –≤ –º–æ–µ–π –±–∞–∑–µ –Ω–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –ø–æ –≤–∞—à–µ–º—É –≤–æ–ø—Ä–æ—Å—É.",
                    "suggestions": "–ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—Ñ—Ä–∞–∑–∏—Ä–æ–≤–∞—Ç—å –≤–æ–ø—Ä–æ—Å –∏–ª–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ —é—Ä–∏—Å—Ç—É."
                }
            
            # –§–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
            results = []
            for i, idx in enumerate(indices[0]):
                if idx < len(self.documents):
                    law_text = self.documents[idx]
                    law_meta = self.metadata[idx]
                    
                    # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å (–ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –≤ –æ—Ü–µ–Ω–∫—É 0-1)
                    relevance = max(0, 1 - distances[0][i] / 10)
                    
                    results.append({
                        "text": law_text,
                        "metadata": law_meta,
                        "relevance": round(relevance, 2),
                        "preview": law_text[:150] + "..." if len(law_text) > 150 else law_text
                    })
            
            # –§–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∏–Ω–∞–ª—å–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞
            response_text = f"üîç –ü–æ –≤–∞—à–µ–º—É –≤–æ–ø—Ä–æ—Å—É –Ω–∞–π–¥–µ–Ω–æ {len(results)} –ø—Ä–∞–≤–æ–≤—ã—Ö –Ω–æ—Ä–º:\n\n"
            
            for i, result in enumerate(results, 1):
                response_text += f"{i}. {result['metadata']['full_reference']}\n"
                response_text += f"   –†–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å: {result['relevance']*100}%\n"
                response_text += f"   –§—Ä–∞–≥–º–µ–Ω—Ç: {result['preview']}\n\n"
            
            response_text += "üìã –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏:\n"
            response_text += "1. –°–æ—Ö—Ä–∞–Ω–∏—Ç–µ –≤—Å–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã, —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Å —Å–∏—Ç—É–∞—Ü–∏–µ–π\n"
            response_text += "2. –ü—Ä–æ–∫–æ–Ω—Å—É–ª—å—Ç–∏—Ä—É–π—Ç–µ—Å—å —Å –ø—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–º —é—Ä–∏—Å—Ç–æ–º\n"
            response_text += "3. –°–æ–±–ª—é–¥–∞–π—Ç–µ —Å—Ä–æ–∫–∏ –æ–±—Ä–∞—â–µ–Ω–∏—è –≤ –≥–æ—Å—É–¥–∞—Ä—Å—Ç–≤–µ–Ω–Ω—ã–µ –æ—Ä–≥–∞–Ω—ã\n\n"
            response_text += "‚ö†Ô∏è –í–∞–∂–Ω–æ: –≠—Ç–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –Ω–æ—Å–∏—Ç –æ–∑–Ω–∞–∫–æ–º–∏—Ç–µ–ª—å–Ω—ã–π —Ö–∞—Ä–∞–∫—Ç–µ—Ä."
            
            return {
                "success": True,
                "query": question,
                "results": results,
                "response": response_text,
                "count": len(results)
            }
            
        except Exception as e:
            return {
                "success": False,
                "message": f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∑–∞–ø—Ä–æ—Å–∞: {str(e)}"
            }


